function loadEvent() {
    alert("페이지가 로드되었습니다");
}

function unloadEvent() {
    alert("페이지를 떠납니다");
}

function clickEvent1() {
    alert("버튼 1을 클릭했습니다.");
}

function clickEvent2() {
    alert("버튼 2를 클릭했습니다.");
}

function focusEvent() {
    window.status="Focus 이벤트 발생 상태 입니다.";
}

function blurEvent() {
    window.status = "Blur 이벤트 발생 상태 입니다.";
}